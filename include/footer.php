                  /*----------------------Live Searching Using Ajax & Php--------------------------*/
                  
                  $(document).ready(function()
                  {
                     load_data();
                     function load_data(search_destination)
                     {
                        $.ajax({
                           url:"<?=PATH?>action.php",
                           method:"POST",
                           data:{search_list:search_destination},
                           success:function(data)
                           {
                              $('#livesearch').html(data);
                           }
                        });
                     }
                     $('#search_destination').keyup(function(){
                        $("#livesearch").show();
                        var search = $(this).val();
                        console.log(search);
                        if(search != '')
                        {
                           load_data(search);
                        }
                        else
                        {
                           $("#livesearch").hide();
                           load_data();
                        }
                     });
                     $(document).on('click','li',function(){
                   $("#search_destination").val($(this).text());
                   $("#livesearch").html();
                   $("#livesearch").hide();
                  });
              });
